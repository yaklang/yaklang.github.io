#!/usr/bin/env node
/*
 * gen-site-packages.js
 *
 * 由 old_site_packages.txt(官网全站打包记账文件) 生成供前端读取的
 * static/site-packages.json。
 *
 * old_site_packages.txt 每行格式:
 *   编号标识<TAB>UTC时间<TAB>zip地址<TAB>条目数 (空行与 # 开头的注释行忽略)
 *
 *   编号标识: 日期(YYYY-MM-DD) 或 日期-N(一天多包时的次序编号)
 *   UTC时间:  ISO 字符串
 *   zip地址:  阿里云 OSS URL
 *   条目数:   打包时统计到的内容文件数量
 *
 * 文件中最新追加在末尾, 输出 JSON 数组按"最新在前"排序:
 *   [{ id, date, url, count }]
 */
const fs = require("fs");
const path = require("path");

const repoRoot = path.resolve(__dirname, "..");
const srcFile = path.join(repoRoot, "old_site_packages.txt");
const outFile = path.join(repoRoot, "static", "site-packages.json");

function parse(content) {
  const items = [];
  const lines = content.split(/\r?\n/);
  for (const raw of lines) {
    const line = raw.trim();
    if (line === "" || line.startsWith("#")) continue;
    const parts = line.split("\t");
    const id = (parts[0] || "").trim();
    if (id === "") continue;
    const date = (parts[1] || "").trim();
    const url = (parts[2] || "").trim();
    const countStr = (parts[3] || "").trim();
    const count = countStr === "" ? null : Number(countStr);
    items.push({
      id,
      date,
      url,
      count: Number.isFinite(count) ? count : null,
    });
  }
  items.reverse();
  return items;
}

function main() {
  let content = "";
  if (fs.existsSync(srcFile)) {
    content = fs.readFileSync(srcFile, "utf8");
  }
  const items = parse(content);
  fs.mkdirSync(path.dirname(outFile), { recursive: true });
  fs.writeFileSync(outFile, JSON.stringify(items, null, 2) + "\n", "utf8");
  console.log(`generated ${outFile} with ${items.length} package(s)`);
}

main();
